#include <stdio.h>

int main(void)

{

unsigned int a = 5 , b = 7;

printf("a = %u and b = %u\n" , a , b);

printf("a&b = %u\n" , a & b);

printf("a|b = %u\n" , a | b);

printf("~a = %u\n" , a = ~a);

printf("a^b = %u\n" , a ^ b);

printf("a<<b = %u\n" , a << b);

printf("a>>b = %u\n" , a >> b);

return 0;

}




























