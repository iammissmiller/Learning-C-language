#include <stdio.h>

int main()
{
    //array declaration and initialization
    int arr[5] = { 15 , 25, 35 ,45 , 55 };
    
    //accessing elements at index 2 i.e 3rd elements
    printf("element of arr[2] : %d\n " ,  arr[2]);
    
    //accessing elements at index 4 i.e last element 
    printf("element of arr[4] : %d\n " ,  arr[4]);
    
    //accesing element at index 0 i.e first element
    printf("element of arr[0] : %d\n " ,  arr[0]);

    return 0;
}
